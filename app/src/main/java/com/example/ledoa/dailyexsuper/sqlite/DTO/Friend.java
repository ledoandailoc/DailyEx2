package com.example.ledoa.dailyexsuper.sqlite.DTO;

public class Friend {
	int avatar;
	String tkhoan;
	String tthai;
	int FriendAccountId;
	public int getAvatar() {
		return avatar;
	}
	public void setAvatar(int avatar) {
		this.avatar = avatar;
	}
	public String getTkhoan() {
		return tkhoan;
	}
	public void setTkhoan(String tkhoan) {
		this.tkhoan = tkhoan;
	}
	public String getTthai() {
		return tthai;
	}
	public void setTthai(String tthai) {
		this.tthai = tthai;
	}
	public int getFriendAccountId() {
		return FriendAccountId;
	}
	public void setFriendAccountId(int friendAccountId) {
		FriendAccountId = friendAccountId;
	}
	
}
